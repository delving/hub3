func TestNativeASCIIFolding(t *testing.T) {
	type args struct {
		text string
	}

	tests := []struct {
		name string
		args args
		want string
	}{
		{
			"e with accent",
			args{"privé"},
			"prive",
		},
		{
			"c with accent",
			args{"curaçao"},
			"curacao",
		},
		{
			"Japanese text",
			args{"行くぞ"},
			"行くそ",
		},
	}

	for _, tt := range tests {
		tt := tt
		t.Run(tt.name, func(t *testing.T) {
			got := NativeASCIIFolding(tt.args.text)
			if diff := cmp.Diff(tt.want, got); diff != "" {
				t.Errorf("NativeASCIIFolding(); %s = mismatch (-want +got):\n%s", tt.name, diff)
			}
		})
	}
}
