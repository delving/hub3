func Test_LuceneASCIIFolding(t *testing.T) {
	is := is.New(t)

	for _, tt := range foldingTests {
		tt := tt

		t.Run(tt.name, func(t *testing.T) {
			text, err := strconv.Unquote("\"" + tt.escapedRune + "\"")
			is.NoErr(err)

			if got := LuceneASCIIFolding(text); got != tt.want {
				t.Errorf("foldAscii() %s = %v, want %v", tt.name, got, tt.want)
			}
		})
	}
}

